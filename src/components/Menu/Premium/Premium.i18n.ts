// --- Helper: get translation with fallback to 'en' ---
export const getT = (lang: string): Record<string, string> =>
  premiumI18n[lang] || premiumI18n['en'];

// --- Helper: interpolate template strings like "Tối đa {max} chuyên viên" ---
export const tpl = (template: string, vars: Record<string, string | number>): string =>
  Object.entries(vars).reduce(
    (str, [key, val]) => str.replace(`{${key}}`, String(val)),
    template
  );

